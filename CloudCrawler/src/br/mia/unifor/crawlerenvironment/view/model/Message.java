package br.mia.unifor.crawlerenvironment.view.model;

public class Message {
	public Queue queue;
	public String body;

	public Queue getQueue() {
		return queue;
	}

	public void setQueue(Queue queue) {
		this.queue = queue;
	}

	public String getBody() {
		return body;
	}

	public void setBody(String body) {
		this.body = body;
	}
}
